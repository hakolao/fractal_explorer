#include "libft.h"

size_t	ft_lstlen(t_list *lst)
{
	t_list	*tmp;
	size_t	i;

	if (!lst)
		return (0);
	tmp = lst;
	i = 0;
	while (tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
